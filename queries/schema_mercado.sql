    sku VARCHAR(255),
    ean VARCHAR(255),
    nome VARCHAR(255),
    nome_pdv VARCHAR(255),
    cadastro_at TIMESTAMP,
    ultima_compra_at TIMESTAMP,
    ultima_venda_at TIMESTAMP,
    edited_at TIMESTAMP,
    preco_ultima_compra NUMERIC(15,4),
    preco_venda NUMERIC(15,4),
    preco_custo NUMERIC(15,4),
    fator_multiplicativo NUMERIC(15,4),
    cean_no_fornecedor VARCHAR(255),
    stock NUMERIC(15,4),
    unidade_venda VARCHAR(50),
    balanca_integrada INTEGER,
    imagem TEXT,
    id_grupo INTEGER,
    qtd_por_caixa NUMERIC(15,4),
    cest VARCHAR(50),
    ncm VARCHAR(50),
    id_regra_icms INTEGER,
    id_grupo_ipi INTEGER,
